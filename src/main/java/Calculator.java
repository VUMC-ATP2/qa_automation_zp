public class Calculator {

    public double sum(double x, double y){
        return  x+y;
    }

    public double subtract(double x, double y){
        return  x-y;
    }

    public double devide(double x, double y){
        return  x/y;
    }

    public double multiply(double x, double y){
        return  x*y;
    }
}
